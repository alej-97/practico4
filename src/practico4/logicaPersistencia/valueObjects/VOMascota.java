package practico4.logicaPersistencia.valueObjects;

public class VOMascota {
	private String apodo;
	private String raza;
	
	public VOMascota(String apodo, String raza) {
		super();
		this.apodo = apodo;
		this.raza = raza;
	}
	
	public String getApodo() {
		return apodo;
	}
	
	public String getRaza() {
		return raza;
	}
	
	
}
